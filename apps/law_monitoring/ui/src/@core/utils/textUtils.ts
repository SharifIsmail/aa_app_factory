export function escapeHtml(input: string): string {
  return input
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

export function escapeRegex(input: string): string {
  // Escape characters with special meaning in regular expressions
  return input.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

